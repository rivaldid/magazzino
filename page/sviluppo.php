<h1> Bozza Magazzino Data Center </h1>
<hr>

<div style="text-align:justify; padding:20px 120px;">
<p>
	<b>IL MAGAZZINO</b> nasce dall'esigenza di tracciare il materiale in arrivo presso il datacenter di torino, esigenza che si configura in particolar modo 
	nello stoccaggio di materiali per scopi futuri. Nasce da un lavoro del gruppo accessi del datacenter di torino, i quali erano improntati però alla gestione 
	materiale della merce, la gestione logica è stata affidata a questo progetto. Il lavoro si divide in due sotto progetti: uno che si occupa della parte dati, 
	ed un altro che si occupa della parte interattiva.
</p>
<p>
	<b>L'IDEA</b> è lasciare il carico di lavoro alla parte dati, quindi inserimenti risposte eccezioni con i dati saranno trasparenti alla parte interattiva, 
	la quale riceverà solo stream di dati. in ogni fase di lavoro è sempre marcata la natura di separare le due componenti.
</p>
<p>
	<b>LO SVILUPPO</b> ha seguito un approccio per prototipi successivi, formalizzata un'attività e relativi casi d'uso si è passati ad implementazioni minimali da
	sottoporre alle attività designate.
</p>
